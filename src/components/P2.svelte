<h1 class="text-2xl group flex whitespace-pre-wrap">2. 사용하지 않는 가장 작은 숫자 찾기</h1>
<p>
	0부터 시작하는 양수(+)인 ID가 있습니다. 여러 ID를 보고 사용하지 않는 가장 작은 숫자를 찾는 함수를
	작성해주세요.
</p>
<br />

<h3 class="text-lg">예시</h3>
<ul>
	<li>입력: [0, 1, 2, 4] -> 출력: 3</li>
	<li>입력: [0, 1, 2, 3, 4, 5, 6] -> 출력: 7</li>
</ul>

<h3 class="text-lg">주의</h3>
<ul>
	<li>정렬되지 않는 배열을 넣어도 동작해야 합니다.</li>
	<li>중복된 숫자가 있는 경우에도 동작해야 합니다.</li>
</ul>
