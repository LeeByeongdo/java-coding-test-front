<h1 class="text-2xl group flex whitespace-pre-wrap">3. 승점 구하기</h1>
<p>
	점수를 보고 이기면 3점, 비기면 1점, 지면 0점으로 계산하여 전체 승점을 구하는 함수를 작성해주세요.
</p>
<br />

<h3 class="text-lg">예시</h3>
<ul>
	<li>입력: ["3:1", "2:2", "1:3"] -> 출력: 4</li>
</ul>

<h3 class="text-lg">주의</h3>
<ul>
	<li>10개의 경기 점수를 입력합니다.</li>
	<li>0점에서 최대 4점까지 입력합니다.</li>
</ul>
